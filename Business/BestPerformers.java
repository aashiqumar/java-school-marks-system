package Business;

/**
 *
 * @author aashiq
 */
public class BestPerformers {
    
    private String high1;
    private String high2;
    private String high3;

    public BestPerformers() {
    }

    public BestPerformers(String high1, String high2, String high3) {
        this.high1 = high1;
        this.high2 = high2;
        this.high3 = high3;
    }

    public String getHigh1() {
        return high1;
    }

    public void setHigh1(String high1) {
        this.high1 = high1;
    }

    public String getHigh2() {
        return high2;
    }

    public void setHigh2(String high2) {
        this.high2 = high2;
    }

    public String getHigh3() {
        return high3;
    }

    public void setHigh3(String high3) {
        this.high3 = high3;
    }
    
 
    
}
